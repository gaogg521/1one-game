function toTrad(s) {
  let out = s;
  for (const [a, b] of tradPairs) out = out.split(a).join(b);
  return out;
}

function pick(loc) {
  if (loc === "en" || loc === "ms" || loc === "th") return { hud: hudEn, banner: bannerEn };
  if (loc === "zh-Hans") return { hud: hudZh, banner: bannerZh };
  return {
    hud: deepMapStrings(hudZh, toTrad),
    banner: deepMapStrings(bannerZh, toTrad),
  };
}

for (const p of paths) {
  const loc = p.split("/").pop().replace(".json", "");
  const j = JSON.parse(fs.readFileSync(p, "utf8"));
  const { hud, banner } = pick(loc);
  j.gameEvents.hud = hud;
  j.gameEvents.banner = banner;
  fs.writeFileSync(p, `${JSON.stringify(j, null, 2)}\n`);
}

