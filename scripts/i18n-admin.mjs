function toZhHant(obj) {
  const hant = (v) =>
    v
      .replace(/运营/g, "營運")
      .replace(/审核/g, "審核")
      .replace(/精选/g, "精選")
      .replace(/用户/g, "用戶")
      .replace(/加载/g, "載入")
      .replace(/权限/g, "權限")
      .replace(/验证/g, "驗證")
      .replace(/暂无/g, "暫無")
      .replace(/转化/g, "轉化")
      .replace(/订阅/g, "訂閱")
      .replace(/额度/g, "額度")
      .replace(/发额度/g, "發額度")
      .replace(/发放/g, "發放")
      .replace(/昵称/g, "暱稱")
      .replace(/邀请/g, "邀請")
      .replace(/隐藏/g, "隱藏")
      .replace(/公开/g, "公開")
      .replace(/漫画/g, "漫畫")
      .replace(/小说/g, "小說")
      .replace(/游戏/g, "遊戲")
      .replace(/复制链接/g, "複製連結")
      .replace(/短链点击/g, "短鏈點擊")
      .replace(/未知/g, "未知")
      .replace(/分享事件/g, "分享事件")
      .replace(/裂变注册/g, "裂變註冊")
      .replace(/付费订单/g, "付費訂單")
      .replace(/注册礼包/g, "註冊禮包")
      .replace(/套餐购买/g, "套餐購買")
      .replace(/运营发放/g, "營運發放")
      .replace(/长篇生成/g, "長篇生成");

  return Object.fromEntries(
    Object.entries(obj).map(([k, v]) => [
      k,
      v && typeof v === "object" && !Array.isArray(v) ? toZhHant(v) : typeof v === "string" ? hant(v) : v,
    ]),
  );
}

